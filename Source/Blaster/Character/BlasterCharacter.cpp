void ABlasterCharacter::Elim()
{
	if (Combat)
	{
		if (Combat->EquippedWeapon)
		{
			if (Combat->EquippedWeapon->bDestroyedWeapon)
			{
				Combat->EquippedWeapon->Destroy();
			}
			else
			{
				Combat->EquippedWeapon->Dropped();
			}
		}
		if (Combat->SecondaryWeapon)
		{
			if (Combat->SecondaryWeapon->bDestroyedWeapon)
			{
				Combat->SecondaryWeapon->Destroy();
			}
			else
			{
				Combat->SecondaryWeapon->Dropped();
			}
		}

		
	}

	MulticastElim();
	GetWorldTimerManager().SetTimer(
		ElimTimer,
		this,
		&ABlasterCharacter::ElimTimerFinished,
		ElimDelay
	);
}

void ABlasterCharacter::MulticastElim_Implementation()
{
	if(BlasterPlayerController)
	{
		BlasterPlayerController->SetHUDWeaponAmmo(0);
		BlasterPlayerController->SetHUDWeaponName(EWeaponType::EWT_MAX);
		BlasterPlayerController->SetHUDCarriedAmmo(0);
	}

	bElimmed = true;
	PlayElimMontage();

	if (DynamicDissolveMaterialInstance_1 && DynamicDissolveMaterialInstance_2)
	{
		//Element 1
		DynamicDissolveMaterialInstance_1->SetScalarParameterValue(TEXT("Dissolve"), 0.f);
		DynamicDissolveMaterialInstance_1->SetScalarParameterValue(TEXT("Dissolve Glow"), 500.f);

		//Element 2
		DynamicDissolveMaterialInstance_2->SetScalarParameterValue(TEXT("Dissolve"), 0.f);
		DynamicDissolveMaterialInstance_2->SetScalarParameterValue(TEXT("Dissolve Glow"), 500.f);
	}

	StartDissolve();

	//Disable CharacterMovement;
	bDisableGameplay = true;
	GetCharacterMovement()->DisableMovement();
	if(Combat)
	{
		Combat->FireButtonPressed(false);
	}

	bool bHideSniperScope = IsLocallyControlled() &&
		Combat && Combat->bAiming &&
		Combat->EquippedWeapon && Combat->EquippedWeapon->GetWeaponType() == EWeaponType::EWT_Sniper;

	if(bHideSniperScope)
	{
		ShowSniperScopeWidget(false);
	}


	//DisableCollision
	GetCapsuleComponent()->SetCollisionEnabled(ECollisionEnabled::NoCollision);
	GetMesh()->SetCollisionEnabled(ECollisionEnabled::NoCollision);

	//Spawn Elim Bot
	if(ElimBotEffect)
	{
		FVector ElimBotSpawnPoint(GetActorLocation().X, GetActorLocation().Y, GetActorLocation().Z + 200.f);
		UGameplayStatics::SpawnEmitterAtLocation(
			GetWorld(),
			ElimBotEffect,
			ElimBotSpawnPoint,
			GetActorRotation()
			);
	}
	if(ElimBotSound)
	{
		UGameplayStatics::SpawnSoundAtLocation(
			this,
			ElimBotSound,
			GetActorLocation()
		);
	}
}

void ABlasterCharacter::ElimTimerFinished()
{
	ABlasterGamemode* BlasterGamemode = GetWorld()->GetAuthGameMode < ABlasterGamemode >();
	if (BlasterGamemode)
	{
		BlasterGamemode->RequestRespawn(this, Controller);
	}
}

void ABlasterCharacter::TurnInPlace(float DeltaTime)
{
	if (AO_Yaw > 90.f)
	{
		TurningInPlace = ETurningInPlace::ETIP_Right;
	}
	else if (AO_Yaw < -90.f)
	{
		TurningInPlace = ETurningInPlace::ETIP_Left;
	}
	if (TurningInPlace != ETurningInPlace::ETIP_NotTurning)
	{
		Interp_AO_Yaw = FMath::FInterpTo(Interp_AO_Yaw, 0.f, DeltaTime, 4.f);
		AO_Yaw = Interp_AO_Yaw;
		if (FMath::Abs(AO_Yaw) < 15.f)
		{
			TurningInPlace = ETurningInPlace::ETIP_NotTurning;
			StartingAimRotation = FRotator(0.f, GetBaseAimRotation().Yaw, 0.f);
		}
	}
}

